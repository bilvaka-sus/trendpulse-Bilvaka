def get_category(title):
    title = title.lower()

    if any(word in title for word in ["ai","software","tech","code","computer","data","cloud","api","gpu","llm","startup","app","programming"]):
        return "technology"

    elif any(word in title for word in ["war","government","country","president","election","climate","attack","global","policy","economy","minister"]):
        return "worldnews"

    elif any(word in title for word in ["nfl","nba","fifa","sport","game","team","player","league","match","tournament","championship"]):
        return "sports"

    elif any(word in title for word in ["research","study","space","physics","biology","discovery","nasa","genome","science","experiment"]):
        return "science"

    elif any(word in title for word in ["movie","film","music","netflix","game","book","show","award","streaming","series","tv"]):
        return "entertainment"

    return None

