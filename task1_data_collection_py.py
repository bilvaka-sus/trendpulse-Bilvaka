import requests

url = "https://hacker-news.firebaseio.com/v0/topstories.json"
headers = {"User-Agent": "TrendPulse/1.0"}

response = requests.get(url, headers=headers)

if response.status_code == 200:
    data = response.json()
    story_ids = data[:2000]
    print("IDs loaded:", len(story_ids))
else:
    print("Failed")

collected_data = []

from datetime import datetime
import time

collected_data = []

for story_id in story_ids:
    try:
        # Stop when we reach exactly 100
        if len(collected_data) == 100:
            break

        item_url = f"https://hacker-news.firebaseio.com/v0/item/{story_id}.json"
        res = requests.get(item_url, headers=headers)

        if res.status_code != 200:
            continue

        story = res.json()

        if not story or "title" not in story:
            continue

        category = get_category(story["title"])

        # If no category, skip (keeps it clean)
        if not category:
            continue

        data_item = {
            "post_id": story.get("id"),
            "title": story.get("title"),
            "category": category,
            "score": story.get("score", 0),
            "num_comments": story.get("descendants", 0),
            "author": story.get("by"),
            "collected_at": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        }

        collected_data.append(data_item)

        time.sleep(0.1)

    except:
        continue

print(len(collected_data))

import json
import os
from datetime import datetime

if not os.path.exists("data"):
    os.makedirs("data")

filename = f"data/trends_{datetime.now().strftime('%Y%m%d')}.json"

with open(filename, "w") as f:
     json.dump(collected_data, f, indent=4)

print(f"Collected {len(collected_data)} stories. Saved to {filename}")

